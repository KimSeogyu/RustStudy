/**
 * [해볼만한 과제]
 * 화씨와 섭씨를 상호 변환.
 * n번째 피보나치 수열 생성.
 * 크리스마스 캐롤 “The Twelve Days of Christmas”의 가사를 반복문을 활용해 출력.
 */
fn main() {
    let mut x = 5;

    println!("The value of x is: {}", x);

    x = 6;

    println!("The value of x is: {}", x);

    const MAX_FEE: u32 = 100_000;

    println!("The MAX_FEE is: {}", MAX_FEE);

    let shadowed_x = 5;
    let shadowed_x = shadowed_x + 1;
    let shadowed_x = shadowed_x + 1;

    println!("The value of x shadowed_x is : {}", shadowed_x);
}
